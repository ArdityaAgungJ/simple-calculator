    public void Calculate(String[] number, String ekpr){
        String ans="";
        if (number[0].isEmpty()){
            if (ekpr.equals("root"))
                ans=String.valueOf(Math.sqrt(Double.parseDouble(number[2])));
            else if ((ekpr.equals("sin"))){
                ans=String.valueOf(Math.sin(Math.toRadians(Double.parseDouble(number[2]))));
            }
            else if ((ekpr.equals("cos")))
                ans=String.valueOf(Math.cos(Math.toRadians(Double.parseDouble(number[2]))));
        } else {
            if (ekpr.equals("X"))
                ans=String.valueOf((Double.parseDouble(number[0]))*(Double.parseDouble(number[1])));
            else if (ekpr.equals(":"))
                ans=String.valueOf((Double.parseDouble(number[0]))/(Double.parseDouble(number[1])));
            else if (ekpr.equals("+"))
                ans=String.valueOf((Double.parseDouble(number[0]))+(Double.parseDouble(number[1])));
            else if (ekpr.equals("-"))
                ans=String.valueOf((Double.parseDouble(number[0]))-(Double.parseDouble(number[1])));
        }
        display.setText(ans);
        
    }
