    private void bEqualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEqualActionPerformed
        String txtDisplay = display.getText().trim();
        String number[] = txtDisplay.split("");
        String ekpr="";
        if (txtDisplay.contains("root")){
            number=txtDisplay.split("root|\\(|\\)");
            ekpr="root";
        } else if (txtDisplay.contains("sin")){
            number=txtDisplay.split("sin|\\(|\\)");
            ekpr="sin";
        } else if (txtDisplay.contains("cos")){
            number=txtDisplay.split("cos|\\(|\\)");
            ekpr="cos";
        } else if (txtDisplay.contains("X")){
            number=txtDisplay.split("X");
            ekpr="X";
        } else if (txtDisplay.contains(":")){
            number=txtDisplay.split("\\:");
            ekpr=":";
        } else if (txtDisplay.contains("+")){
            number=txtDisplay.split("\\+");
            ekpr="+";
        } else if (txtDisplay.contains("-")){
            number=txtDisplay.split("\\-");
            ekpr="-";
        } else {
            JOptionPane.showMessageDialog(this, "Ekspresi "+txtDisplay+" Salah !", "Message",1);
        }
            Calculate(number, ekpr);
    }//GEN-LAST:event_bEqualActionPerformed

